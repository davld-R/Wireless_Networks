      public void Agregarusuario(){
          
          String pass = String.valueOf(txt_pass.getPassword());
          String SQL = "insert into usuarios (usuario,pass) values(?,?)";
          
           try {
            
            String url = "jdbc:mysql://localhost:3306/gimnasio";
            String usuario = "root";
            String contraseña = "DsJnTv20DS";  
            
               
             Class.forName("com.mysql.jdbc.Driver").newInstance(); 
             con = DriverManager.getConnection(url,usuario,contraseña); 
             if ( con != null ) 
                    System.out.println("Se ha establecido una conexión a la base de datos " +  
                                       "\n " + url ); 
             
               PreparedStatement stmt = con.prepareStatement(SQL);
               
               stmt.setString(1, txt_usuario.getText());
               stmt.setString(2, pass);
               
               stmt.executeUpdate();
               JOptionPane.showMessageDialog(null, "Registro Exitoso");
               
               this.txt_usuario.setText("");
               this.txt_pass.setText("");
                              
                              
        }
        catch (Exception e) {
            
            JOptionPane.showMessageDialog(null,"Error de registro " + e.getMessage());
        }
      }

